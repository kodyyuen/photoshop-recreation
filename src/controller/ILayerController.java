package controller;

/**
 * Represents the interface that is the controller of the LayerModel. This interface provides a way
 * to start the program in which the users can enter their preferred way of entering commands
 * (console or script) and can use various commands to create a multi-layered image and manipulate
 * its layers.
 */
public interface ILayerController {

  /**
   * EFFECT: Starts the program so that the user can use it.
   */
  void start();
}
